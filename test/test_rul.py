class TestRUL(unittest.TestCase):
    def setUp(self):
        self.dp = DataPreprocessor()
        self.rul = RUL(data_processor=self.dp)
        self.rul.preprocess_data()

    def test_load_and_cleanup(self):
        data, labels = self.rul.load_data(0)
        self.assertTrue(data is not None, "Data not None")
        self.assertTrue(labels is not None, "Labels not None")
        self.assertTrue(len(labels) == len(data), "Data and labels have same length")

        # Before column remove
        self.assertTrue(len(data[0]) == 26)
        RUL._skip_column(data=data, col_index=0)
        self.assertTrue(len(data[0]) == 25)
